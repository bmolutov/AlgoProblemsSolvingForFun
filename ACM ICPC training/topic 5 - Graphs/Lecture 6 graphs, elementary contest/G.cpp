#include <bits/stdc++.h>
using namespace std;

void solve () {
    int n;
    cin >> n;
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n; j++) {
            int x;
            cin >> x;
            if (x == 1 && i < j) {
                cout << i << ' ' << j << '\n';
            }
        }
    }
}

